package com.dbconnectionwithhibernate.entities.user_entity;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.Id;
import jakarta.persistence.Table;

@Entity
@Table(name = "user")
public class User {
    @Id
    @Column(columnDefinition = "varchar(36)", length = 36, nullable = false, unique = true)
    private String id;

    @Column(columnDefinition = "varchar(255)", length = 255, nullable = false)
    private String name;

    public User() {
    }

    public User(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
